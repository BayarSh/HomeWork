# Родительский класс для животных
class Animal:
    def __init__(self, name):
        self.alive = True  # Живое состояние животного
        self.fed = False   # Состояние, указывающее, накормлено ли животное
        self.name = name   # Имя животного

# Родительский класс для растений
class Plant:
    def __init__(self, name):
        self.edible = False  # Съедобность растения (по умолчанию - несъедобное)
        self.name = name     # Имя растения

# Класс для млекопитающих, наследует от Animal
class Mammal(Animal):
    def eat(self, food):
        # Метод, позволяющий животному есть
        # Проверяем, съедобное ли переданное растение
        if food.edible:
            print(f"{self.name} съел {food.name}")  # Сообщаем, что животное съело растение
            self.fed = True  # Устанавливаем состояние на "накормлено"
        else:
            print(f"{self.name} не стал есть {food.name}")  # Сообщаем, что животное не ело
            self.alive = False  # Устанавливаем состояние на "погибло"

# Класс для хищников, наследует от Animal
class Predator(Animal):
    def eat(self, food):
        # Метод, позволяющий хищнику есть
        # Проверяем, съедобное ли переданное растение
        if food.edible:
            print(f"{self.name} съел {food.name}")  # Сообщаем, что хищник съел растение
            self.fed = True  # Устанавливаем состояние на "накормлено"
        else:
            print(f"{self.name} не стал есть {food.name}")  # Сообщаем, что хищник не ест
            self.alive = False  # Устанавливаем состояние на "погибло"

# Класс для цветов, наследует от Plant
class Flower(Plant):
    def __init__(self, name):
        # Инициализация атрибутов класса Plant вручную
        self.edible = False     # Цветы не съедобные
        self.name = name        # Имя цветка

# Класс для фруктов, наследует от Plant
class Fruit(Plant):
    def __init__(self, name):
        # Инициализация атрибутов класса Plant вручную
        self.edible = True      # Фрукты съедобные
        self.name = name        # Имя фрукта

# Пример использования классов
a1 = Predator('Волк с Уолл-Стрит')  # Создаем хищника
a2 = Mammal('Хатико')                # Создаем млекопитающее
p1 = Flower('Цветик семицветик')     # Создаем цветок
p2 = Fruit('Заводной апельсин')      # Создаем фрукт

# Выводим имена объектов
print(a1.name)  # Вывод имени хищника
print(p1.name)  # Вывод имени цветка

# Проверяем состояние животных
print(a1.alive)  # Проверяем, жив ли хищник (должен быть True)
print(a2.fed)    # Проверяем, накормлено ли млекопитающее (должно быть False)

# Хищник пытается съесть цветок
a1.eat(p1)      # Проверяем, что происходит, когда хищник пытается съесть цветок
# Млекопитающее ест фрукт
a2.eat(p2)      # Проверяем, что происходит, когда млекопитающее ест фрукт

# Проверяем состояние животных после еды
print(a1.alive)  # Проверяем, жив ли хищник после попытки поесть (должен быть False)
print(a2.fed)    # Проверяем, накормлено ли млекопитающее после еды (должно быть True)
